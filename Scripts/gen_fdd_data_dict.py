def make_kv_sheet(wb, name, fields, sheet_title=None, is_first=False):
    """Create a key-value sheet: col A=字段编码, col B=值"""
    if is_first:
        ws = wb.active
        ws.title = name
    else:
        ws = wb.create_sheet(name)

    ws.column_dimensions['A'].width = 25
    ws.column_dimensions['B'].width = 55

    ws.append(["字段编码", "值"])
    ws['A1'].font = header_font
    ws['B1'].font = header_font
    ws['A1'].fill = header_fill
    ws['B1'].fill = header_fill

    if sheet_title:
        ws.append([sheet_title, ""])

    for field, default in fields:
        ws.append([field, default])

    return ws


def make_table_sheet(wb, name, columns, row_count=3):
    """Create a table sheet with column headers and empty rows"""
    ws = wb.create_sheet(name)
    for ci, col_name in enumerate(columns, 1):
        cell = ws.cell(row=1, column=ci, value=col_name)
        cell.font = header_font
        cell.fill = header_fill
        cell.alignment = center_align
        ws.column_dimensions[openpyxl.utils.get_column_letter(ci)].width = 22
    return ws


